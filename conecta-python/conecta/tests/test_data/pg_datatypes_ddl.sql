create table pg_datatypes
(
    id                serial primary key,
    small_int         smallint,
    int_              integer,
    big_int           bigint,
    decimal_          numeric(10, 2),
    numeric_          numeric(12, 4),
    real_             real,
    double_           double precision,
    char_             char,
    bpchar_           char(5),
    varchar_          varchar(50),
    text_             text,
    bool_             boolean,
    date_             date,
    time_             time,
    timetz_           time with time zone,
    timestamp_        timestamp,
    timestamptz_      timestamp with time zone,
    interval_ interval,
    json_             json,
    jsonb_            jsonb,
    uuid_             uuid,
    bytea_            bytea,
    -- geo
    point_            point,
    circle_           circle,
    line_             line,
    box_              box,

    int_array         integer[],
    text_array        text[],
    smallint_array    smallint[],
    bigint_array      bigint[],
    decimal_array     numeric(10, 2)[],
    numeric_array     numeric(12, 4)[],
    real_array        real[],
    double_array      double precision[],
    char_array        char(5)[],
    varchar_array     varchar(50)[],
    bool_array        boolean[],
    date_array        date[],
    time_array        time without time zone[],
    timetz_array      time with time zone[],
    timestamp_array   timestamp without time zone[],
    timestamptz_array timestamp with time zone[],
    interval_array interval[],
    json_array        json[],
    jsonb_array       jsonb[],
    uuid_array        uuid[],
    bytea_array       bytea[],
    point_array       point[],
    circle_array      circle[],
    int_range         int4range,
    num_range         numrange,
    ts_range          tsrange,
    int4range_array   int4range[],
    numrange_array    numrange[],
    tsrange_array     tsrange[]
);

